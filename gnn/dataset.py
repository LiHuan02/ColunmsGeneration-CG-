"""VCSP bipartite graph dataset for GNN training.

Loads .npz files generated by data_generation, each containing one CG iteration's
bipartite graph with column/constraint features, edges, and MILP labels.
"""

import glob
import os
import numpy as np
import torch
from torch.utils.data import Dataset


class VCSPBipartiteDataset(Dataset):
    """Dataset of bipartite graphs from VCSP column generation iterations.

    Each sample is a single CG iteration with:
      - column_features: (n_cols, d) tensor
      - constraint_features: (n_constraints, p) tensor
      - edge_index: (2, n_edges) tensor [col_idx, constr_idx]
      - labels: (n_cols,) tensor (only meaningful for new columns)
      - new_col_mask: (n_cols,) bool tensor
    """

    def __init__(self, data_dir, normalize=True):
        """Load all .npz files from data_dir subdirectories.

        Args:
            data_dir: root directory containing instance_XXXX/ subdirectories
            normalize: if True, normalize features to zero mean unit variance
        """
        self.data_dir = data_dir
        self.samples = []
        self.normalize = normalize
        self._feature_stats = None  # Computed during loading if normalize=True

        # Collect all .npz files
        pattern = os.path.join(data_dir, 'instance_*', 'iter_*.npz')
        self.file_list = sorted(glob.glob(pattern))

        if len(self.file_list) == 0:
            raise ValueError(f"No .npz files found in {data_dir}")

        print(f"Found {len(self.file_list)} data files in {data_dir}")

        # Pre-compute feature statistics for normalization
        if self.normalize:
            self._compute_normalization_stats()

    def _compute_normalization_stats(self):
        """Compute mean/std across all column and constraint features."""
        col_sum = np.zeros(12, dtype=np.float64)
        col_sq_sum = np.zeros(12, dtype=np.float64)
        constr_sum = np.zeros(2, dtype=np.float64)
        constr_sq_sum = np.zeros(2, dtype=np.float64)
        n_col_total = 0
        n_constr_total = 0

        for fpath in self.file_list:
            data = np.load(fpath, allow_pickle=True)
            cf = data['column_features'].astype(np.float64)
            ctf = data['constraint_features'].astype(np.float64)
            col_sum += cf.sum(axis=0)
            col_sq_sum += (cf ** 2).sum(axis=0)
            constr_sum += ctf.sum(axis=0)
            constr_sq_sum += (ctf ** 2).sum(axis=0)
            n_col_total += cf.shape[0]
            n_constr_total += ctf.shape[0]

        col_mean = col_sum / n_col_total
        col_std = np.sqrt(col_sq_sum / n_col_total - col_mean ** 2)
        col_std = np.maximum(col_std, 1e-8)  # Avoid division by zero

        constr_mean = constr_sum / n_constr_total
        constr_std = np.sqrt(constr_sq_sum / n_constr_total - constr_mean ** 2)
        constr_std = np.maximum(constr_std, 1e-8)

        self._feature_stats = {
            'col_mean': col_mean.astype(np.float32),
            'col_std': col_std.astype(np.float32),
            'constr_mean': constr_mean.astype(np.float32),
            'constr_std': constr_std.astype(np.float32),
        }

    def _normalize(self, col_feat, constr_feat):
        """Apply z-score normalization."""
        if self._feature_stats is None:
            return col_feat, constr_feat
        col_feat = (col_feat - self._feature_stats['col_mean']) / self._feature_stats['col_std']
        constr_feat = (constr_feat - self._feature_stats['constr_mean']) / self._feature_stats['constr_std']
        return col_feat, constr_feat

    def __len__(self):
        return len(self.file_list)

    def __getitem__(self, idx):
        """Load and return a single data point."""
        fpath = self.file_list[idx]
        data = np.load(fpath, allow_pickle=True)

        col_feat = torch.from_numpy(data['column_features'].astype(np.float32))
        constr_feat = torch.from_numpy(data['constraint_features'].astype(np.float32))
        edge_index = torch.from_numpy(data['edge_index'].astype(np.int64))
        labels = torch.from_numpy(data['labels'].astype(np.int64))
        new_col_mask = torch.from_numpy(data['new_col_mask'].astype(bool))

        if self.normalize:
            col_feat_np = col_feat.numpy()
            constr_feat_np = constr_feat.numpy()
            col_feat_np = (col_feat_np - self._feature_stats['col_mean']) / self._feature_stats['col_std']
            constr_feat_np = (constr_feat_np - self._feature_stats['constr_mean']) / self._feature_stats['constr_std']
            col_feat = torch.from_numpy(col_feat_np)
            constr_feat = torch.from_numpy(constr_feat_np)

        return {
            'column_features': col_feat,
            'constraint_features': constr_feat,
            'edge_index': edge_index,
            'labels': labels,
            'new_col_mask': new_col_mask,
        }

    @staticmethod
    def collate_fn(batch):
        """Collate function that returns list of samples (graphs have different sizes)."""
        return batch

    def save_normalization_stats(self, path):
        """Save normalization stats for use during inference."""
        if self._feature_stats is not None:
            np.savez(path, **self._feature_stats)

    def load_normalization_stats(self, path):
        """Load normalization stats."""
        data = np.load(path)
        self._feature_stats = {
            'col_mean': data['col_mean'],
            'col_std': data['col_std'],
            'constr_mean': data['constr_mean'],
            'constr_std': data['constr_std'],
        }
